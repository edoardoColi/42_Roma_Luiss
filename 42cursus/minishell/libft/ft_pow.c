#include "libft.h"

long	ft_pow(int num, int n)
{
	long	res;

	if (num == 0)
		return (0);
	if (n == 0)
		return (1);
	res = num;
	n--;
	while (n-- > 0)
		res = res * num;
	return (res);
}
